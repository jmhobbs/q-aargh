<p>
	Q-Aargh is a simple concept where digital content meets the physical realm via <a href="http://en.wikipedia.org/wiki/QR_Code" target="_blank">QR codes</a>.
</p>
<p>
	The principal concept of Q-Aargh is similar to the hobby known as <a href="http://en.wikipedia.org/wiki/Letterboxing" target="_blank">letterboxing</a>.  In letterboxing people create special stamps and hide them, then distribute clues for finding that letterbox. Other people then go around trying to find letterboxes and stamping their own notebooks with the ones they find.
</p>
<p>
	Q-Aargh can be used in the same way.  You generate a QR code that leads to your "island", a page on the Q-Aargh site. Next, you hide that QR code somewhere.  Others find and scan it, which leads them to your island and lets them see your digital content and post their own.
</p>
<p>
	Of course, you don't have to use it that way at all. If you have a clever idea for how to use it, please go right ahead!
</p>